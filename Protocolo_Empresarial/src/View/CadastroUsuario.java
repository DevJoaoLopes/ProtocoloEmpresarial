    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        int v[] = tbUsuarios.getSelectedRows();
        List<Usuario> c = new LinkedList<>();

        for(int i=0;i<v.length;i++)
        {
            int idxTabela = v[i];
            int idxList = tbUsuarios.convertRowIndexToModel(idxTabela);
            c.add(lstUsuarios.get(idxList));
        }

        lstUsuarios.removeAll(c);
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
      
        Usuario r = new Usuario();
        if(tbUsuarios.getSelectedRows().length==0){
            r.setNome(txtNome.getText());
            r.setEmail(txtEmail.getText());
            r.setRg(txtRg.getText());
            r.setCpf(txtCpf.getText());
            lstUsuarios.add(r);
            txtNome.setText("");
            txtEmail.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            ControllerUsuario c = new ControllerUsuario();
            c.insertUsuario(r);
        }else{
            lstUsuarios.add(r);
            tbUsuarios.getSelectionModel().setSelectionInterval(
                lstUsuarios.size()-1,
                lstUsuarios.size()-1);
        }

    }//GEN-LAST:event_btAdicionarActionPerformed

